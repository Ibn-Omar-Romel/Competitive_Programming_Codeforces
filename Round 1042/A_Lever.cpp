void solve() {

    int n;
    cin >> n;
    vector<int> a(n), b(n);
    for (int i = 0; i < n; i++) cin >> a[i];
    for (int i = 0; i < n; i++) cin >> b[i];

    int count = 0;
    
    for (int i = 0; i < n; i++) {
        if (a[i] > b[i]) {
            count += (a[i]-b[i]);
        }   
    }
    
    

    cout << count+1 << "\n";

}




